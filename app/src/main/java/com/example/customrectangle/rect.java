package com.example.customrectangle;

public class rect {

    public float l, b;

    rect(){

        l=0;
        b=0;


    }

    public rect(float l, float b) {
        this.l = l;
        this.b = b;
    }

    public float getL() {
        return l;
    }

    public void setL(float l) {
        this.l = l;
    }

    public float getB() {
        return b;
    }

    public void setB(float b) {
        this.b = b;
    }


}
